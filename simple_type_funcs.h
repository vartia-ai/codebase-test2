// simple_type_funcs.h

// Public data structure types
typedef char my_char_type;
typedef unsigned char my_uchar_type;
typedef int my_int_type;
typedef unsigned int my_uint_type;
typedef long my_long_type;
typedef unsigned long my_ulong_type;
typedef float my_float_type;
typedef double my_double_type;

// Public function prototypes
char return_char(char value);
unsigned char return_uchar(unsigned char value);
int return_int(int value);
unsigned int return_uint(unsigned int value);
long return_long(long value);
unsigned long return_ulong(unsigned long value);

float return_float(float value);
double return_double(double value);

my_char_type return_my_char_type(my_char_type value);
my_uchar_type return_my_uchar_type(my_uchar_type value);
my_int_type return_my_int_type(my_int_type value);
my_uint_type return_my_uint_type(my_uint_type value);
my_long_type return_my_long_type(my_long_type value);
my_ulong_type return_my_ulong_type(my_ulong_type value);
